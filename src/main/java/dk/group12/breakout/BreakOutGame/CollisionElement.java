package dk.group12.breakout.BreakOutGame;

public class CollisionElement {
    public double x;
    public double y;
    public double width;
    public double height;

    public CollisionElement(double x, double y, double width, double height){
        this.x = x;
        this.y = y;
        this.width = width;
        this.height = height;
    }

}
